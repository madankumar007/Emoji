from turtle import*
width(5)
speed(5)

begin_fill()
fillcolor('yellow')
circle(200)
end_fill()

penup()
goto(-100,200)
pendown()

begin_fill()
fillcolor('black')
circle(30)
end_fill()

penup()
goto(100,200)
pendown()

begin_fill()
fillcolor('black')
circle(30)
end_fill()

penup()
goto(0,10)
pendown()

begin_fill()
fillcolor('black')
circle(80)
end_fill()

done()


